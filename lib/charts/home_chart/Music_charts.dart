  static ChartModel getChartByGenre(String genre) {
    switch (genre) {
      case 'Pop Vibes':
        return ChartModel(
          chartName: "Top Pop Vibes",
          chartItems: popChart,
          lastUpdated: DateTime.now(),
        );
      case 'Hip-Hop Heat':
        return ChartModel(
          chartName: "Top Hip-Hop Heat",
          chartItems: hipHopChart,
          lastUpdated: DateTime.now(),
        );
      case 'Rock Legends':
        return ChartModel(
          chartName: "Top Rock Legends",
          chartItems: rockChart,
          lastUpdated: DateTime.now(),
        );
      default:
        return ChartModel(
          chartName: "Unknown Genre",
          chartItems: [],
          lastUpdated: DateTime.now(),
        );
    }
  }
